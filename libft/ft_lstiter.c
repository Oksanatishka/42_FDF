#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *flwr;

	flwr = lst;
	while (flwr != NULL)
	{
		f(flwr);
		flwr = flwr->next;
	}
}
