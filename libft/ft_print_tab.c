#include "libft.h"

void	ft_print_tab(char **s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		ft_putnbr(i);
		ft_putchar('\n');
		ft_putendl(s[i]);
		ft_putchar('\n');
		i++;
	}
}
